//- factorial-for.js -> Este archivo debe calcular el factorial de 10 utilizando un solo bucle for

let n=10;
let total=10;

for(let i=n-1;i>=1;i--){
    total = total*i;
}
console.log(total);


let num=1;
let resultado=10;

for(let i=num+1;i<10;i++){
    resultado=resultado*i
}
